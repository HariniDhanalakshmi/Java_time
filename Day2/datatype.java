
public class datatype {
    public static void main(String[] args) {
        int myNum = 5; //primitive data type ( int, float, char, boolean)
        float myFloatNum = 5.99f;
        char myLetter = 'D';
        boolean myBool = true;
        String myText = "Hello"; //non-primitive data type
        System.out.println(myNum + "\n" +myFloatNum + "\n" + myLetter + "\n" + myBool + "\n" + myText);
    }
    
}
